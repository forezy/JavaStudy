package a1prelude;

public class B5NumberSystem {
    public static void main(String args[]){
        // 输出自动转为 10 进制！
        System.out.println(0123); // 83，8进制123，前面加零。

        System.out.println(0b111); //7, 二进制111，加 0b 或者 0B。
        System.out.println(0B111); //7

        System.out.println(0x21AF); //8623,16进制21AF，加 0x 或者 0X。
        System.out.println(0X21AF);
    }
}
